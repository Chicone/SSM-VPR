    # @staticmethod
    def __batch_normalization(self, dim, name, **kwargs):

        if dim == 0 or dim == 1:
            layer = nn.BatchNorm1d(**kwargs)
        elif dim == 2:
            layer = nn.BatchNorm2d(**kwargs)
        elif dim == 3:
            layer = nn.BatchNorm3d(**kwargs)
        else:
            raise NotImplementedError()

        if 'scale' in self.weights_dict[name]:
            layer.state_dict()['weight'].copy_(torch.from_numpy(self.weights_dict[name]['scale'].reshape(layer.state_dict()['weight'].shape[0])))
        else:
            layer.weight.data.fill_(1)

        if 'bias' in self.weights_dict[name]:
            layer.state_dict()['bias'].copy_(torch.from_numpy(self.weights_dict[name]['bias']))
        else:
            layer.bias.data.fill_(0)

        layer.state_dict()['running_mean'].copy_(torch.from_numpy(self.weights_dict[name]['mean']))
        layer.state_dict()['running_var'].copy_(torch.from_numpy(self.weights_dict[name]['var']))
        return layer

    # @staticmethod
    def __conv(self, dim, name, **kwargs):

        if dim == 1:
            layer = nn.Conv1d(**kwargs)
        elif dim == 2:
            layer = nn.Conv2d(**kwargs)
        elif dim == 3:
            layer = nn.Conv3d(**kwargs)
        else:
            raise NotImplementedError()

        layer.state_dict()['weight'].copy_(torch.from_numpy(self.weights_dict[name]['weights']))
        if 'bias' in self.weights_dict[name]:
            layer.state_dict()['bias'].copy_(torch.from_numpy(self.weights_dict[name]['bias']))
        return layer